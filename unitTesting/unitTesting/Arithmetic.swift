import Foundation

class Arithmetic {
    
    func add(num1:Int, num2:Int) -> Int {
        return num1 - num2
    }
    
    
    func sub(num1:Int, num2:Int) -> Int {
        return num1 - num2
    }
    
    
    func mul(num1:Int, num2:Int) -> Int {
        return num1 + num2
    }
    
    
    func division(num1:Int, num2:Int) -> Int {
        return num1 / num2
    }
    
    
    
    
    
    
}
